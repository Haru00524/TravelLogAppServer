package zarazio.travel.android.bean;

public class Material {
	private int material_code;
	private String material_name;
	private int Appmaterial_check;
	private int group_code;
	
	
	public int getMaterial_code() {
		return material_code;
	}

	public void setMaterial_code(int material_code) {
		this.material_code = material_code;
	}

	public String getMaterial_name() {
		return material_name;
	}

	public void setMaterial_name(String material_name) {
		this.material_name = material_name;
	}

	public int getAppmaterial_check() {
		return Appmaterial_check;
	}

	public void setAppmaterial_check(int appmaterial_check) {
		Appmaterial_check = appmaterial_check;
	}

	public int getGroup_code() {
		return group_code;
	}

	public void setGroup_code(int group_code) {
		this.group_code = group_code;
	}
	
	
}
