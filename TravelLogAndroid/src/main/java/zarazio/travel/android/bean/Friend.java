package zarazio.travel.android.bean;

public class Friend {
	String friend_accept;
	String user_id;
	String friend_id;
	String friend_State;
	
	public String getFriend_accept() {
		return friend_accept;
	}

	public void setFriend_accept(String friend_accept) {
		this.friend_accept = friend_accept;
	}
	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getFriend_id() {
		return friend_id;
	}

	public void setFriend_id(String friend_id) {
		this.friend_id = friend_id;
	}

	public String getFriend_State() {
		return friend_State;
	}

	public void setFriend_State(String friend_State) {
		this.friend_State = friend_State;
	}
	
	
}
